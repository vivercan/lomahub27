        const { error: updateError } = await supabase
          .from('documentos_cliente')
          .update({
            nombre_archivo: selectedFile.name,
            storage_path: fileName,
            status: 'subido',
            updated_at: new Date().toISOString(),
            razon_rechazo: null,
          })
          .eq('id', existingDoc.id)

